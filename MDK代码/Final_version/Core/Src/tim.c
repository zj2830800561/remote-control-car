/* TIM1CH2_CAP_STA 各数据位说明
** bit7   捕获完成标志
** bit6	  捕获到高电平标志
** bit5~0 捕获高电平后定时器溢出的次数*/
/* 重写定时器更新中断处理回调函数 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
	if((TIM1CH2_CAP_STA & 0X80) == 0){	//还未成功捕获
		if(TIM1CH2_CAP_STA & 0X40){		//已经捕获到高电平
			if((TIM1CH2_CAP_STA & 0X3F) == 0X3F){	//高电平时间太长了
				TIM1CH2_CAP_STA |= 0X80;			//标记为完成一次捕获
				TIM1CH2_CAP_VAL = 0XFFFF;			//计数器值
			}
			else
				TIM1CH2_CAP_STA++;		//溢出次数加1			
		}	
	}
}
/* 重写输入捕获中断处理回调函数 */
void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim){
	if((TIM1CH2_CAP_STA & 0X80) == 0){	//还未成功捕获
		if(TIM1CH2_CAP_STA & 0X40){		//捕获到上升沿后条件为真
			TIM1CH2_CAP_STA |= 0X80;	//标记为完成一次高电平捕获.
			TIM1CH2_CAP_VAL = HAL_TIM_ReadCapturedValue(&htim1,TIM_CHANNEL_2);	//获取当前的计数器值
			TIM_RESET_CAPTUREPOLARITY(&htim1,TIM_CHANNEL_2);	//清除原来的设置		
			TIM_SET_CAPTUREPOLARITY(&htim1,TIM_CHANNEL_2,TIM_ICPOLARITY_RISING);	//设置上升沿捕获
		}
		else{
			TIM1CH2_CAP_STA = 0;
			TIM1CH2_CAP_VAL = 0;
			TIM1CH2_CAP_STA |= 0X40;	//标记捕获到上升沿
			__HAL_TIM_DISABLE(&htim1);	//关闭定时器
			__HAL_TIM_SET_COUNTER(&htim1,0);	//计数器值清零
			TIM_RESET_CAPTUREPOLARITY(&htim1,TIM_CHANNEL_2);	//清除原来的设置				
			TIM_SET_CAPTUREPOLARITY(&htim1,TIM_CHANNEL_2,TIM_ICPOLARITY_FALLING);	//设置下降沿捕获
			__HAL_TIM_ENABLE(&htim1);	//使能定时器		
		}	
	}
}
